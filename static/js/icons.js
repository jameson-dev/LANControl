// Get icon SVG by name
function getDeviceIcon(iconName) {
    return DEVICE_ICONS[iconName] || DEVICE_ICONS['device'];
}

// Initialize icon picker
function initIconPicker() {
    const picker = document.getElementById('iconPicker');
    if (!picker) return;

    const currentIcon = document.getElementById('deviceIcon').value || 'device';
    let html = '';

    // Render all icons in a grid
    Object.keys(DEVICE_ICONS).forEach(iconName => {
        const isSelected = iconName === currentIcon;
        html += `
            <button type="button"
                    class="icon-option p-3 rounded-lg border-2 transition ${isSelected ? 'border-blue-500 bg-blue-500/20' : 'border-white/10 bg-white/5 hover:bg-white/10'}"
                    data-icon="${iconName}"
                    onclick="selectIcon('${iconName}')"
                    title="${iconName}">
                <div class="w-8 h-8 mx-auto text-gray-300">
                    ${DEVICE_ICONS[iconName]}
                </div>
            </button>
        `;
    });

    picker.innerHTML = html;
}

// Select an icon
function selectIcon(iconName) {
    document.getElementById('deviceIcon').value = iconName;

    // Update visual selection
    document.querySelectorAll('.icon-option').forEach(btn => {
        if (btn.dataset.icon === iconName) {
            btn.className = 'icon-option p-3 rounded-lg border-2 transition border-blue-500 bg-blue-500/20';
        } else {
            btn.className = 'icon-option p-3 rounded-lg border-2 transition border-white/10 bg-white/5 hover:bg-white/10';
        }
    });
}

// Render device icon in UI
function renderDeviceIcon(iconName, size = 'w-6 h-6') {
    const svg = getDeviceIcon(iconName);
    return `<div class="${size} text-gray-400">${svg}</div>`;
}
